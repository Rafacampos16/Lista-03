public class OperadoresRelacionais {
	
	public static void main (String[] args) {
		int a = 1;
		int b = 2;
		
        // a é maior que b
        System.out.println(a > b);  

        // a é igual a b
        System.out.println(a == b);  
        
        // a é menor que b
		System.out.println(a < b);  

        // a é diferente de b
        System.out.println(a != b);  

        // a é maior ou igual a b
        System.out.println(a >= b);
        
        // a é menor ou igual a b
        System.out.println(a <= b);    
	}
}

